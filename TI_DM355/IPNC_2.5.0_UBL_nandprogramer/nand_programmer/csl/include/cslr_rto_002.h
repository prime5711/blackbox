#include <cslr.h>

#include <tistdtypes.h>

/**************************************************************************\
* Register Overlay Structure
\**************************************************************************/
typedef struct  {
    volatile Uint32 REVID;
    volatile Uint32 CTRL_STATUS;
} CSL_RtoRegs;

/**************************************************************************\
* Field Definition Macros
\**************************************************************************/

/* REVID */

#define CSL_RTO_REVID_SCHEME_MASK        (0xC0000000u)
#define CSL_RTO_REVID_SCHEME_SHIFT       (0x0000001Eu)
#define CSL_RTO_REVID_SCHEME_RESETVAL    (0x00000001u)

#define CSL_RTO_REVID_FUNC_MASK          (0x0FFF0000u)
#define CSL_RTO_REVID_FUNC_SHIFT         (0x00000010u)
#define CSL_RTO_REVID_FUNC_RESETVAL      (0x000004D0u)

#define CSL_RTO_REVID_RTL_MASK           (0x0000F800u)
#define CSL_RTO_REVID_RTL_SHIFT          (0x0000000Bu)
#define CSL_RTO_REVID_RTL_RESETVAL       (0x00000000u)

#define CSL_RTO_REVID_MAJOR_MASK         (0x00000700u)
#define CSL_RTO_REVID_MAJOR_SHIFT        (0x00000008u)
#define CSL_RTO_REVID_MAJOR_RESETVAL     (0x00000000u)

#define CSL_RTO_REVID_CUSTOM_MASK        (0x000000C0u)
#define CSL_RTO_REVID_CUSTOM_SHIFT       (0x00000006u)
#define CSL_RTO_REVID_CUSTOM_RESETVAL    (0x00000000u)

#define CSL_RTO_REVID_MINOR_MASK         (0x0000003Fu)
#define CSL_RTO_REVID_MINOR_SHIFT        (0x00000000u)
#define CSL_RTO_REVID_MINOR_RESETVAL     (0x00000003u)

#define CSL_RTO_REVID_RESETVAL           (0x44D00003u)

/* CTRL_STATUS */

#define CSL_RTO_CTRL_STATUS_OUTPUTVALUE_MASK (0x003C0000u)
#define CSL_RTO_CTRL_STATUS_OUTPUTVALUE_SHIFT (0x00000012u)
#define CSL_RTO_CTRL_STATUS_OUTPUTVALUE_RESETVAL (0x00000000u)

#define CSL_RTO_CTRL_STATUS_SOURCEPOLARITY_MASK (0x00020000u)
#define CSL_RTO_CTRL_STATUS_SOURCEPOLARITY_SHIFT (0x00000011u)
#define CSL_RTO_CTRL_STATUS_SOURCEPOLARITY_RESETVAL (0x00000000u)

/*----SOURCEPOLARITY Tokens----*/
#define CSL_RTO_CTRL_STATUS_SOURCEPOLARITY_ACTIVEHIGH (0x00000000u)
#define CSL_RTO_CTRL_STATUS_SOURCEPOLARITY_ACTIVELOW (0x00000001u)

#define CSL_RTO_CTRL_STATUS_OVERRUN_MASK (0x00010000u)
#define CSL_RTO_CTRL_STATUS_OVERRUN_SHIFT (0x00000010u)
#define CSL_RTO_CTRL_STATUS_OVERRUN_RESETVAL (0x00000000u)

#define CSL_RTO_CTRL_STATUS_OPMASKDATA_MASK (0x0000F000u)
#define CSL_RTO_CTRL_STATUS_OPMASKDATA_SHIFT (0x0000000Cu)
#define CSL_RTO_CTRL_STATUS_OPMASKDATA_RESETVAL (0x00000000u)

#define CSL_RTO_CTRL_STATUS_OPPATTERNDATA_MASK (0x00000F00u)
#define CSL_RTO_CTRL_STATUS_OPPATTERNDATA_SHIFT (0x00000008u)
#define CSL_RTO_CTRL_STATUS_OPPATTERNDATA_RESETVAL (0x00000000u)

#define CSL_RTO_CTRL_STATUS_OUTPUTMODE_MASK (0x00000080u)
#define CSL_RTO_CTRL_STATUS_OUTPUTMODE_SHIFT (0x00000007u)
#define CSL_RTO_CTRL_STATUS_OUTPUTMODE_RESETVAL (0x00000000u)

/*----OUTPUTMODE Tokens----*/
#define CSL_RTO_CTRL_STATUS_OUTPUTMODE_DIRECTOUT (0x00000000u)
#define CSL_RTO_CTRL_STATUS_OUTPUTMODE_TOGGLEOUT (0x00000001u)

#define CSL_RTO_CTRL_STATUS_DETECTBIT_MASK (0x00000060u)
#define CSL_RTO_CTRL_STATUS_DETECTBIT_SHIFT (0x00000005u)
#define CSL_RTO_CTRL_STATUS_DETECTBIT_RESETVAL (0x00000000u)

/*----DETECTBIT Tokens----*/
#define CSL_RTO_CTRL_STATUS_DETECTBIT_NO_EVENT (0x00000000u)
#define CSL_RTO_CTRL_STATUS_DETECTBIT_RISING_EDGE_SRC (0x00000001u)
#define CSL_RTO_CTRL_STATUS_DETECTBIT_FALLING_EDGE_SRC (0x0000000Au)
#define CSL_RTO_CTRL_STATUS_DETECTBIT_BOTH_EDGE_SRC (0x0000000Bu)

#define CSL_RTO_CTRL_STATUS_SELECTBIT_MASK (0x0000001Eu)
#define CSL_RTO_CTRL_STATUS_SELECTBIT_SHIFT (0x00000001u)
#define CSL_RTO_CTRL_STATUS_SELECTBIT_RESETVAL (0x00000000u)

/*----SELECTBIT Tokens----*/
#define CSL_RTO_CTRL_STATUS_SELECTBIT_INPUT0 (0x00000000u)
#define CSL_RTO_CTRL_STATUS_SELECTBIT_INPUT1 (0x00000001u)

#define CSL_RTO_CTRL_STATUS_ENABLE_MASK  (0x00000001u)
#define CSL_RTO_CTRL_STATUS_ENABLE_SHIFT (0x00000000u)
#define CSL_RTO_CTRL_STATUS_ENABLE_RESETVAL (0x00000000u)

/*----ENABLE Tokens----*/
#define CSL_RTO_CTRL_STATUS_ENABLE_INACTIVE (0x00000000u)
#define CSL_RTO_CTRL_STATUS_ENABLE_ACTIVE (0x00000001u)

#define CSL_RTO_CTRL_STATUS_RESETVAL     (0x00000000u)

#endif
